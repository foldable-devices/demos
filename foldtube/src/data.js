export function getVideos() {
    // Randomize the video list.
    for (let i = videos_array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        const temp = videos_array[i];
        videos_array[i] = videos_array[j];
        videos_array[j] = temp;
    }
    return videos_array;
}

export function filterVideos(filter) {
    if (filter === undefined)
        return videos_array;
    let results = [];
    for (let i = 0, j = 0; i < videos_array.length - 1; i++) {
        if (!videos_array[i].title.includes(filter))
            continue;
        results[j] = videos_array[i];
        j++;
    }
    return results;
}

export function findVideo(name) {
    for (var i = videos_array.length - 1; i > 0; i--) {
        if (videos_array[i].name === name)
            return videos_array[i];
    }
    return null;
}

